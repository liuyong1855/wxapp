Page({
	data:{
		text:''
	},
	save:function(e){
		wx.setStorage({
			key:'key',
			data:Math.random(),
			success:function(d){
				console.log('save-success')
			}
		})
	},
	get:function(e){
		wx.getStorage({
			key:'key',
			success:function(d){
				console.log(d)
			}
		})
	},
	gets:function(e){
		wx.getStorageInfo({
			success:function(d){
				console.log(d)
			}
		})
	},
	remove:function(e){
		wx.removeStorage({
			key:'key',
			success:function(d){
				console.log(d)
			}
		})
	},
	clear:function(e){
		console.log('清空')
		wx.clearStorage()
	}
})